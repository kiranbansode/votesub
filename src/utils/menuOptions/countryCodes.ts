import { getList } from 'country-list-with-dial-code-and-flag';
import { nanoid } from 'nanoid';

export interface ICountryOption {
    id: string;
    value: string;
    option: string;
    name: string;
}

export const countryCodelabelsCreator = () => {
    const countryCodes = getList();
    const options: ICountryOption[] = [];

    countryCodes.forEach(({ dial_code, name, flag }) => {
        options.push({
            value: dial_code,
            option: `${flag} ${name}: ${dial_code}`,
            id: nanoid(),
            name,
        });
    });

    return options;
};

